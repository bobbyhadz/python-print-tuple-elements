# Print tuple elements in Python

my_tuple = ('one', 'two', 'three')

# ✅ print a tuple
print(my_tuple)  # 👉️ ('one', 'two', 'three')

# ✅ print the first element in a tuple
print(my_tuple[0])  # 👉️ 'one'

# ✅ print the last element in a tuple
print(my_tuple[-1])  # 👉️ 'three'

# ✅ print a slice of a tuple
print(my_tuple[0:2])  # 👉️ ('one', 'two')